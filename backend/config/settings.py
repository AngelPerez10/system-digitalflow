def _load_dotenv_file() -> None:
    """Load backend/.env into os.environ when present (local dev)."""
    env_path = BASE_DIR / '.env'
    if not env_path.exists():
        return
    try:
        for raw_line in env_path.read_text(encoding='utf-8', errors='ignore').splitlines():
            line = raw_line.strip()
            if not line or line.startswith('#') or '=' not in line:
                continue
            k, v = line.split('=', 1)
            k = k.strip()
            v = v.strip().strip('"').strip("'")
            if k and (k not in os.environ or not str(os.environ.get(k, "")).strip()):
                os.environ[k] = v
    except Exception:
        pass


_load_dotenv_file()


def get_env_from_dotenv(key: str) -> str:
    """Lee una clave de backend/.env (útil si runserver arrancó antes de editar el archivo)."""
    env_path = BASE_DIR / '.env'
    if not env_path.exists():
        return ''
    try:
        for raw_line in env_path.read_text(encoding='utf-8', errors='ignore').splitlines():
            line = raw_line.strip()
            if not line or line.startswith('#') or '=' not in line:
                continue
            k, v = line.split('=', 1)
            if k.strip() == key:
                return v.strip().strip('"').strip("'")
    except Exception:
        return ''
    return ''


def _parse_debug_env() -> bool:
    """Read DEBUG from environment; default False for production safety."""
    raw = os.environ.get('DEBUG', '').strip().lower()
    if raw in ('true', '1', 'yes', 'on'):
        return True
    if raw in ('false', '0', 'no', 'off'):
        return False
    return False


